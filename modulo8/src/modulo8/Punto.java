package modulo8;

public class Punto {
	private double posicioX;
	private double posicioY;

	public Punto(double posicioX, double posicioY) {
		this.posicioX = posicioX;
		this.posicioY = posicioY;
	}

	public double getPosicioX() {
		return posicioX;
	}

	public double getPosicioY() {
		return posicioY;
	}
}
